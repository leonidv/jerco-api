package jerco.network.io;

public class JercoReaderException extends Exception {
    private static final long serialVersionUID = -8817154914093592060L;

    public JercoReaderException() {
        super();
    }

    public JercoReaderException(String message, Throwable cause) {
        super(message, cause);
    }

    public JercoReaderException(String message) {
        super(message);
    }

    public JercoReaderException(Throwable cause) {
        super(cause);
    }

}
